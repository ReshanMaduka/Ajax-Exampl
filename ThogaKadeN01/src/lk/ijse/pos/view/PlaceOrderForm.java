    private void loadCustomerIds() {
        try {
            ArrayList<String>customerIds=PlaceOrderController.getAllCustomerIds();
            for (String customerId : customerIds) {
                customerIdComboBox.addItem(customerId);
            }
        } catch (Exception ex) {
            Logger.getLogger(PlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadItemCodes() {
        try {
            ArrayList <ItemDTO>itemList=PlaceOrderController.getAllItems();
            for (ItemDTO item : itemList) {
                itemCodeComboBox.addItem(item.getCode());
            }
        } catch (Exception ex) {
            Logger.getLogger(PlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
