    private void clearTextFields(){
        txtCustomerId.setText("");
        txtCustomerName.setText("");
        txtCustomerAddress.setText("");
        txtCustomerSalary.setText("");
    }
    
    private void loadAllCustomers(){
        
        try {
            ArrayList<CustomerDTO> allCustomers = ManageCustomerController.getAllCustomers();
            
            DefaultTableModel dtm = (DefaultTableModel) tblCustomers.getModel();
            dtm.setRowCount(0);
            
            for (CustomerDTO customer : allCustomers) {
                Object[] rowData = {
                    customer.getId(),
                    customer.getName(),
                    customer.getAddress(),
                    customer.getSalary()
                };
                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

