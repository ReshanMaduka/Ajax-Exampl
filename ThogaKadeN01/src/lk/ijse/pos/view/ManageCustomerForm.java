    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        try {
            if (tblCustomers.getSelectedRow() == -1) return;
            
            boolean result = ManageCustomerController.deleteCustomer(tblCustomers.getValueAt(tblCustomers.getSelectedRow(), 0).toString());
            
            if (result){
                JOptionPane.showMessageDialog(this, "Customer has been deleted successfully");
            }else{
                JOptionPane.showMessageDialog(this, "Failed to delete the customer");
            }
            
            clearTextFields();
            tblCustomers.getSelectionModel().clearSelection();
            loadAllCustomers();
            update = false;
            
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

