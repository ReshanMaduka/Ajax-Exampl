    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        CustomerDTO customer = new CustomerDTO(txtCustomerId.getText(),
                txtCustomerName.getText(), 
                txtCustomerAddress.getText(),Double.parseDouble(txtCustomerSalary.getText()));
        
        if (!update){
            try {
                boolean result = ManageCustomerController.saveCustomer(customer);
                if (result){
                    JOptionPane.showMessageDialog(this, "Customer has been added successfully");
                }else{
                    JOptionPane.showMessageDialog(this, "Failed to add the customer");
                }
            } catch (Exception ex) {
                Logger.getLogger(ManageCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            
            try {
                if (tblCustomers.getSelectedRow() == -1) return;
                
                customer.setId((String) tblCustomers.getValueAt(tblCustomers.getSelectedRow(), 0));
                boolean result = ManageCustomerController.updateCustomer(customer);
                
                if (result){
                    JOptionPane.showMessageDialog(this, "Customer has been updated successfully");
                }else{
                    JOptionPane.showMessageDialog(this, "Failed to update the customer");
                }
            } catch (Exception ex) {
                Logger.getLogger(ManageCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        update = false;
        tblCustomers.clearSelection();
        clearTextFields();
        loadAllCustomers();
        
    }//GEN-LAST:event_btnSaveActionPerformed

